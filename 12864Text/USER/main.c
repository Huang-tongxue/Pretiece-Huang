const u8 First[]={"Hello Wordl !!"};
const u8 Second[]={"Welcom  ^_^"};
const u8 Third[]={"你已经专注了:"};
const u8 maohao[]={":"};
u8 seconds_t = 0;
u8 seconds_o = 0;
u8 min_t = 0;
u8 min_o = 0;
u8 hour_t = 0;
u8 hour_o = 0;

/* 
 * 函数名：main
 * 描述  : 主函数
 * 输入  ：无
 * 输出  : 无
 */


int main(void)
{

	/* 设置系统时钟为72M */
	SystemInit();
	 	
	LCD12864_GPIO_Init();
	delay_ms(10);
	KEY_EXTI_Init();
	delay_ms(10);
	
	LCD12864_Init();
	delay_ms(10); 
	
	lcd_pos(0,0);
	write_hz(First);
	
	lcd_pos(1,0);
	write_hz(Second);
	
	lcd_pos(2,0);
	write_hz(Third);
  	delay_ms(100);
	while(1)
	{
		lcd_pos(3,0);//4
		write_data(48+hour_t);//3
		lcd_pos(3,1);//4
		write_data(48+hour_o);//时3
		lcd_pos(3,2);//4
		write_hz(maohao);//:3
		
		lcd_pos(3,3);
		write_data(48+min_t);
		lcd_pos(3,4);
		write_data(48+min_o);//分
		lcd_pos(3,5);
		write_hz(maohao);//:
		
		lcd_pos(3,6);
		write_data(48+seconds_t);
		lcd_pos(3,7);
		write_data(48+seconds_o);//秒
//**************************************************************
//计算
//**************************************************************
		++seconds_o;
		if(seconds_o == 10)
		{
			seconds_o = 0;
			seconds_t++;
		}
		if(seconds_t == 6)
		{
			seconds_t = 0;
			seconds_o = 0;
			++min_o;
		}
		if(min_o == 10){
			min_o = 0;
			min_t ++;
		}
		if(min_t == 6)
		{
			min_o = 0;
			min_t = 0;
			++hour_o;
		}
		if(hour_o == 10){
			hour_o = 0;
			hour_t++;
		}
		if(hour_t == 2 && hour_o == 4)
		{
			hour_t = 0;
			min_t = 0;
			seconds_t = 0;
			hour_o = 0;
			min_o = 0;
			seconds_o = 0;
		}
		
		delay_ms(670);
	}
}

